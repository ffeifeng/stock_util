"""演示 x[len(x):] = [1, 2] 的切片赋值（在列表末尾空切片处插入）"""

x = [3, 5, 7]

print("=" * 50)
print("初始 x =", x)
L = len(x)
print(f"len(x) = {L}")
print(f"切片 x[len(x):] 等同于 x[{L}:]，即从下标 {L} 到列表末尾。")
print(f"当前元素下标为 0..{L - 1}，没有下标 {L}，故 x[{L}:] 选中的是「空的一段」。")
print("在 Python 里，给这样一段「空切片」赋一个序列，会把该序列插入到此位置，")
print("等价于在末尾扩展列表（与 extend / += 类似）。")
print()
print("执行：x[len(x):] = [1, 2]")
print("  求值顺序：先算此时的 len(x)==3，再对 x[3:] 赋 [1, 2]。")
x[len(x):] = [1, 2]
print("执行后 x =", x)
print("=" * 50)
